-- Q26. Identify orders where the current order amount is greater than the previous order amount for the same customer.
SELECT O.ORDER_ID,O.CUSTOMER_ID,P.PAYMENT_AMOUNT AS CURR_AMOUNT,
CASE
	WHEN P.PAYMENT_AMOUNT>LAG(P.PAYMENT_AMOUNT) OVER(ORDER BY O.ORDER_DATE) THEN 'GREATER'
    WHEN P.PAYMENT_AMOUNT=LAG(P.PAYMENT_AMOUNT) OVER(ORDER BY O.ORDER_DATE) THEN 'EQUAL'
    WHEN P.PAYMENT_AMOUNT<LAG(P.PAYMENT_AMOUNT) OVER(ORDER BY O.ORDER_DATE) THEN 'LOWER'
    ELSE NULL
END AS COMP_BTW_CURR_AND_PREV_AMOUNT
FROM ORDERS O JOIN PAYMENTS P ON O.ORDER_ID=P.ORDER_ID
WHERE P.STATUS='SUCCESS';

-- Q27. Calculate the difference in days between consecutive orders for each customer.
SELECT ORDER_ID,CUSTOMER_ID,ORDER_DATE AS CURR_DATE, 
DATEDIFF(ORDER_DATE,LAG(ORDER_DATE) OVER(PARTITION BY CUSTOMER_ID ORDER BY ORDER_DATE)) AS DIFF_BTW_CURR_AND_PERV_ORDER_DATE
FROM ORDERS;

-- Q28. Determine whether each customer's order trend is increasing or decreasing compared to their previous order.
SELECT O.ORDER_ID,O.CUSTOMER_ID,P.PAYMENT_AMOUNT AS CURR_AMOUNT,
CASE
	WHEN P.PAYMENT_AMOUNT>LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 'GREATER'
    WHEN P.PAYMENT_AMOUNT=LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 'EQUAL'
    WHEN P.PAYMENT_AMOUNT<LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 'LOWER'
    ELSE NULL
END AS COMP_BTW_CURR_AND_PREV_AMOUNT_PER_CUST
FROM ORDERS O JOIN PAYMENTS P ON O.ORDER_ID=P.ORDER_ID
WHERE P.STATUS='SUCCESS'
ORDER BY
CASE
	WHEN P.PAYMENT_AMOUNT>LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 1
    WHEN P.PAYMENT_AMOUNT=LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 2
    WHEN P.PAYMENT_AMOUNT<LAG(P.PAYMENT_AMOUNT) OVER(PARTITION BY O.CUSTOMER_ID ORDER BY O.ORDER_DATE) THEN 3
    ELSE 4
END;

