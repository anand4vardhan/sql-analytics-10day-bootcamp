-- Q1. Assign a unique sequential number to each order in the dataset.
SELECT *, ROW_NUMBER() OVER(ORDER BY ORDER_ID) AS RN FROM ORDERS;

-- Q2. For each customer, assign a sequence number to their orders based on order date.
SELECT C.*,O.ORDER_ID,O.ORDER_DATE, ROW_NUMBER() OVER(PARTITION BY C.CUSTOMER_ID ORDER BY ORDER_DATE) AS RN FROM
CUSTOMERS C JOIN ORDERS O ON C.CUSTOMER_ID=O.CUSTOMER_ID;

-- Q3. Display orders such that the most recent order of each customer appears first with a sequence number.
SELECT C.*,O.ORDER_ID,O.ORDER_DATE, ROW_NUMBER() OVER(PARTITION BY C.CUSTOMER_ID ORDER BY ORDER_DATE DESC) AS RN FROM
CUSTOMERS C JOIN ORDERS O ON C.CUSTOMER_ID=O.CUSTOMER_ID;

-- Q4. Rank all orders based on amount, ensuring that ties result in gaps in ranking.
SELECT O.ORDER_ID,P.PAYMENT_ID,P.PAYMENT_AMOUNT, RANK() OVER(ORDER BY P.PAYMENT_AMOUNT) AS PAYMENT_RANK
FROM ORDERS O JOIN PAYMENTS P ON
O.ORDER_ID=P.ORDER_ID;

-- Q5. Rank orders for each customer based on amount without skipping rank numbers for ties.
SELECT O.CUSTOMER_ID,O.ORDER_ID,P.PAYMENT_AMOUNT, RANK() OVER(PARTITION BY O.CUSTOMER_ID ORDER BY P.PAYMENT_AMOUNT) AS PR
FROM ORDERS O JOIN PAYMENTS P ON
O.ORDER_ID=P.ORDER_ID;

-- Q6. Identify the top 3 highest value orders in the entire dataset.
