-- Q21. Create a CTE to rank employees by salary within each department and retrieve top 3 earners per department.
WITH DEPT_SAL_RANK AS
(
	SELECT E.EMP_ID,E.NAME,E.DEPT_ID,D.DEPT_NAME,E.SALARY,ROW_NUMBER() OVER(PARTITION BY D.DEPT_NAME ORDER BY E.SALARY DESC) AS RNK
    FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID
)
SELECT * FROM DEPT_SAL_RANK WHERE RNK<4;

-- Q22. Use a CTE to calculate cumulative salary expense by department over time (running total).
WITH CUME_CTE AS
(
	SELECT E.EMP_ID,E.NAME,E.DEPT_ID,D.DEPT_NAME,E.HIRE_DATE,E.SALARY, SUM(E.SALARY) OVER(PARTITION BY D.DEPT_NAME ORDER BY E.HIRE_DATE) AS CUMM_SUM
    FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID
)
SELECT * FROM CUME_CTE;

-- Q23. Use a CTE to identify duplicate employee records based on same name and department. LET'S SAY DUPS ARE ON SAME NAME,AGE,DEPT,SAL,HIRE
