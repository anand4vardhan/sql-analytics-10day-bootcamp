-- Q14. Use a CTE to calculate total salary per department and display department-wise totals.
WITH DEPT_CTE AS
(
	SELECT D.DEPT_NAME,SUM(E.SALARY) AS DEPT_WISE_TOTAL FROM EMPLOYEES E JOIN DEPARTMENTS D 
    ON E.DEPT_ID=D.DEPT_ID GROUP BY D.DEPT_NAME ORDER BY SUM(E.SALARY)
)
SELECT * FROM DEPT_CTE;

-- Q15. Use a CTE to find the average salary of each department and list employees along with their department average.
WITH DEPT_CTE AS
(
	SELECT D.DEPT_NAME,AVG(E.SALARY) AS DEPT_WISE_AVG FROM EMPLOYEES E JOIN DEPARTMENTS D 
    ON E.DEPT_ID=D.DEPT_ID GROUP BY D.DEPT_NAME ORDER BY AVG(E.SALARY)
)
SELECT E1.*,D1.DEPT_NAME,D2.DEPT_WISE_AVG FROM 
EMPLOYEES E1 JOIN DEPARTMENTS D1 ON E1.DEPT_ID=D1.DEPT_ID
JOIN DEPT_CTE D2 ON D1.DEPT_NAME=D2.DEPT_NAME;

