-- Q25. Use nested CTEs to first filter high-paying departments and then list employees in those departments.
WITH HIGH_PAYING_DEPT AS
(
	SELECT D.DEPT_NAME FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID 
    GROUP BY D.DEPT_NAME ORDER BY AVG(SALARY) DESC LIMIT 3
),
LIST_OF_EMP_IN_HIGH_PAYING_DEPT AS
(
	SELECT E.EMP_ID,E.NAME,E.SALARY,D.DEPT_NAME FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID
    WHERE D.DEPT_NAME IN (SELECT * FROM HIGH_PAYING_DEPT)
)
SELECT * FROM LIST_OF_EMP_IN_HIGH_PAYING_DEPT;

-- Q26. Use a CTE to calculate department-wise revenue and then join with employees to show contribution.
WITH DEPTWISE_TOTAL_SALARY_CTE AS
(
	SELECT D.DEPT_NAME, SUM(E.SALARY)AS DEPT_SAL_TOTAL
    FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID GROUP BY D.DEPT_NAME
), EMP_CONTRI_CTE AS
(
	SELECT E.EMP_ID,E.NAME,D.DEPT_NAME,E.SALARY,(E.SALARY/D2.DEPT_SAL_TOTAL)*100 AS PER_EMP_CONTRI_IN_DEPT
    FROM EMPLOYEES E JOIN DEPARTMENTS D ON E.DEPT_ID=D.DEPT_ID
    JOIN DEPTWISE_TOTAL_SALARY_CTE D2 ON D.DEPT_NAME=D2.DEPT_NAME
)
SELECT * FROM EMP_CONTRI_CTE ORDER BY PER_EMP_CONTRI_IN_DEPT DESC;

