-- Q11. Compute weighted moving average over time. Tables: metrics(entity_id, ts, value)
SELECT * FROM METRICS; -- X AND Y ARE THE WEIGHT
-- RUNNING WEIGHTED AVG
SELECT *, SUM(VALUE*X*Y) OVER(PARTITION BY ENTITY_ID ORDER BY TS) AS MOVING_WEIGHTED_AVG FROM METRICS;

-- Q12. Find longest consecutive activity streak per user. Tables: user_activity(user_id, activity_date)
SELECT *, row_number() over(partition by user_id order by activity_date) as rn FROM USER_ACTIVITY where user_id=30 ORDER BY USER_ID,ACTIVITY_DATE ;
SELECT *, LAG(ACTIVITY_DATE) OVER(PARTITION BY USER_ID ORDER BY ACTIVITY_DATE) AS PREV_ACTIVITY FROM USER_ACTIVITY;
WITH ACTIVITY_CTE AS
(
	SELECT *, LAG(ACTIVITY_DATE) OVER(PARTITION BY USER_ID ORDER BY ACTIVITY_DATE) AS PREV_ACTIVITY,
    DATEDIFF(ACTIVITY_DATE,LAG(ACTIVITY_DATE) OVER(PARTITION BY USER_ID ORDER BY ACTIVITY_DATE)) AS DIFF_BTW_SESSIONS
    FROM USER_ACTIVITY
), SESSION_CTE AS
(
	SELECT *,
    CASE WHEN DIFF_BTW_SESSIONS IS NULL OR DIFF_BTW_SESSIONS>1 THEN 1 ELSE 0 END AS NEW_SESSION
    FROM ACTIVITY_CTE
),USER_STREAK AS
(
	SELECT *, SUM(NEW_SESSION) OVER(PARTITION BY USER_ID ORDER BY ACTIVITY_DATE) AS SESSION_NUMBER
    FROM SESSION_CTE
)
 SELECT USER_ID,MIN(ACTIVITY_DATE) AS FIRST_DAY,MAX(ACTIVITY_DATE) AS LAST_DAY, SESSION_NUMBER,COUNT(*) AS DAYS_IN_STREAK FROM USER_STREAK
 GROUP BY USER_ID,SESSION_NUMBER ORDER BY COUNT(*) DESC;

