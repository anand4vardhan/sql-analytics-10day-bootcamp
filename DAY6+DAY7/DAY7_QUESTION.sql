-- Q20. Generate business-day calendar excluding holidays. Tables: holidays(holiday_date)
SELECT * FROM HOLIDAYS;
WITH RECURSIVE DATES AS
(
	SELECT DATE('2024-01-01') AS DT
    UNION ALL 
    SELECT DT+ INTERVAL 1 DAY
    FROM DATES WHERE DT<'2025-12-31'
)
SELECT *,DAYNAME(DT),
CASE 
	WHEN UPPER(DAYNAME(DT)) IN ('SUNDAY','SATURDAY') THEN 'WEEKEND'
    WHEN DT IN (SELECT HOLIDAY_DATE FROM HOLIDAYS) THEN (SELECT MAX(DESCRIPTION) FROM HOLIDAYS WHERE HOLIDAY_DATE=DT)
    ELSE 'WORKING DAYS'
    END AS CALENDER
FROM DATES;
