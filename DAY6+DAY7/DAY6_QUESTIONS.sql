-- Q4. Compute cumulative distinct product count over time. Tables: sales(product_id, sale_date)
WITH CTEY AS
(
SELECT P.PROD_ID,S.SALE_DATE,ROW_NUMBER() OVER(PARTITION BY P.PROD_ID ORDER BY S.SALE_DATE) AS RNK FROM PRODUCTS P JOIN SALES S
ON P.PROD_ID=S.PRODUCT_ID ORDER BY P.PROD_ID
),FIRST_OCCURANCE AS
(
SELECT SALE_DATE FROM CTEY WHERE RNK=1 ORDER BY SALE_DATE
),
PRODUCT_PER_DAY AS
(	
	SELECT SALE_DATE,COUNT(*) AS NEW_PROD FROM FIRST_OCCURANCE
    GROUP BY SALE_DATE ORDER BY SALE_DATE
) 
SELECT SALE_DATE,SUM(NEW_PROD) OVER(ORDER BY SALE_DATE) AS CUME_DISTINCT FROM PRODUCT_PER_DAY;

