-- Q1. Find the top 3 customers by total annual spend for each year. Tables: orders(customer_id, order_date, amount)
WITH TOP3 AS
(
	SELECT C.CUSTOMER_ID,YEAR(O.ORDER_DATE) AS YEAR_OF_ORDER,SUM(O.AMOUNT) AS TOTAL_REV FROM CUSTOMERS C JOIN ORDERS O 
    ON C.CUSTOMER_ID=O.CUSTOMER_ID GROUP BY C.CUSTOMER_ID, YEAR(O.ORDER_DATE) ORDER BY SUM(O.AMOUNT) DESC LIMIT 3
)
SELECT * FROM TOP3;

-- Q2. Detect patients with gaps of more than 90 days between visits. Tables: visits(patient_id, visit_date)
WITH CTEX AS
(
SELECT *,LAG(VISIT_DATE) OVER(PARTITION BY PATIENT_ID ORDER BY VISIT_DATE) AS PREV_VISIT_DATE,
DATEDIFF(VISIT_DATE,LAG(VISIT_DATE) OVER(PARTITION BY PATIENT_ID ORDER BY VISIT_DATE)) AS DAYS_BTW_CONSECUTIVE_VISIT
 FROM VISITS
) SELECT * FROM CTEX WHERE DAYS_BTW_CONSECUTIVE_VISIT>90;

