-- Q6. Pivot monthly sales into columns. Tables: sales(store_id, sale_date, amount)
SELECT STORE_ID,UPPER(CONCAT(SUBSTR(MONTHNAME(SALE_DATE),1,3),'-',YEAR(SALE_DATE))) AS 'MON-YYYY',SUM(AMOUNT) FROM SALES WHERE STORE_ID=2
GROUP BY STORE_ID, UPPER(CONCAT(SUBSTR(MONTHNAME(SALE_DATE),1,3),'-',YEAR(SALE_DATE)));

-- LET SAY WE NEED PIVOTED RESULT FOR STORE 2 FOR JAN-2025,FEB-2025,MAR-2025, LET'S FIRST CREATE A CTE, SO THAT WE CAN USE IT FURTHER FOR EASE
WITH PIV_CTE AS
(
	SELECT STORE_ID,UPPER(CONCAT(SUBSTR(MONTHNAME(SALE_DATE),1,3),'-',YEAR(SALE_DATE))) AS SALEDATE,AMOUNT FROM SALES
)
SELECT STORE_ID,
		SUM(CASE WHEN SALEDATE='JAN-2025' THEN AMOUNT ELSE 0 END) AS 'JAN-2025',
        SUM(CASE WHEN SALEDATE='FEB-2025' THEN AMOUNT ELSE 0 END) AS 'FEB-2025',
        SUM(CASE WHEN SALEDATE='MAR-2025' THEN AMOUNT ELSE 0 END) AS 'MAR-2025'
FROM PIV_CTE
GROUP BY STORE_ID;

-- Q7. Identify customers with anomalous purchase amounts. Tables: orders(customer_id, amount) -- WILL USE IQR TO FIND OUTLIER,
-- X IS OUTLIER IF X< Q1-1.5*IQR OR X>Q3+1.5*IQR, Q1 IS 25 PERCENTILE, Q3 IS 75TH PERCENTILE AND IQR IS Q3-Q1
WITH CUME_CTE AS
(
SELECT AMOUNT, CUME_DIST() OVER(ORDER BY AMOUNT) AS CUME FROM ORDERS
), IQR AS
(
	 SELECT 
		MIN(CASE WHEN CUME=0.25 THEN AMOUNT END) AS Q1,  -- WHEN NEED TO GET 1 ROW, WE CAN USE MIN OR MAX TO GET THAT ROW
        MIN(CASE WHEN CUME=0.75 THEN AMOUNT END) AS Q3
	FROM CUME_CTE
), OUTLIER_AMOUNT AS
(
	SELECT Q1-1.5*(Q3-Q1) AS MIN_RANGE,
    Q3+1.5*(Q3-Q1) AS MAX_RANGE
    FROM IQR
) SELECT CUSTOMER_ID,ORDER_ID,AMOUNT AS OUTLIER_AMOUNT FROM ORDERS WHERE AMOUNT<(SELECT MIN_RANGE FROM OUTLIER_AMOUNT)
OR AMOUNT>(SELECT MIN_RANGE FROM OUTLIER_AMOUNT) ORDER BY AMOUNT;
    

-- Q8. Remove duplicate products keeping latest record. Tables: products(prod_id, sku, updated_at)
-- LET'S SEE THE TABLE FIRST
SELECT * FROM PRODUCTS;
-- FIND OUT IF THERE ARE MULTIPLE ROWS FOR SAME SKU AND PRODUCT_NAME
WITH ROW_CTE AS
(
SELECT *, ROW_NUMBER() OVER(PARTITION BY SKU,NAME ORDER BY UPDATED_AT DESC) AS RN FROM PRODUCTS
) 
SELECT * FROM ROW_CTE R1 WHERE R1.RN <> (SELECT MAX(R2.RN) FROM ROW_CTE R2 WHERE R1.PROD_ID=R2.PROD_ID);

-- Q9. Sessionize user events by inactivity >30 mins. Tables: events(user_id, event_time)
WITH TIME_CTE AS
(
SELECT *,TIMESTAMPDIFF(MINUTE,EVENT_TIME,LEAD(EVENT_TIME) OVER(PARTITION BY USER_ID ORDER BY EVENT_TIME)) AS NEXT_EVENT_DIFF_IN_MINS,
ROW_NUMBER() OVER(PARTITION BY USER_ID ORDER BY EVENT_TIME) AS RN FROM EVENTS
) 
SELECT *
 FROM TIME_CTE;
 
 WITH time_cte AS (
    SELECT 
        *,
        TIMESTAMPDIFF(
            MINUTE,
            LAG(event_time) OVER (PARTITION BY user_id ORDER BY event_time),
            event_time
        ) AS diff_mins
    FROM events
),

session_flag AS (
    SELECT *,
        CASE 
            WHEN diff_mins IS NULL OR diff_mins > 30 THEN 1
            ELSE 0
        END AS new_session
    FROM time_cte
),

sessionized AS (
    SELECT *,
        SUM(new_session) OVER (
            PARTITION BY user_id 
            ORDER BY event_time
        ) AS session_id
    FROM session_flag
)

SELECT *
FROM sessionized;

